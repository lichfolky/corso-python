# Calcola l'area di un rettangolo, partendo da base e altezza inserite

testo_base = input("inserisci la base: ")
testo_altezza = input("inserisci la altezza: ")
base = float(testo_base)
altezza = float(testo_altezza)
area = base * altezza
testo_area = str(area)
print("l'area del rettangolo è di " + testo_area)

